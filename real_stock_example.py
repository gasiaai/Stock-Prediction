def get_real_stock_data(symbols, start_date='2020-01-01', end_date='2024-12-31'):
    """
    ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡∏∏‡πâ‡∏ô‡∏à‡∏£‡∏¥‡∏á‡∏à‡∏≤‡∏Å Yahoo Finance
    """
